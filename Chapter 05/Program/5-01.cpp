#include <iostream>
using namespace std;
int main(){
 int num = 4;//khai bao va gan gai tri cho bien num
 cout << "The variable num is " << num << endl;//xuat ra gia tri cua bien num
 cout << "I will now increment num.\n\n";
 num++;//gia tri bien num tang len 1 don vi
 cout << "Now the variable num is " << num << endl;//xuat ra gia tri cua bien num
 cout << "I will increment num again.\n\n";
 ++num;//gia tri bien num tang len 1 don vi
 cout << "Now the variable num is " << num << endl;//xuat ra gia tri cua bien num
 cout << "I will now decrement num.\n\n";
 num--;//gia tri bien num gian xuong 1 don vi
 cout << "Now the variable num is " << num << endl;//xuat ra gia tri cua bien num
 cout << "I will decrement num again.\n\n";
 --num;//gia tri bien num giam xuong 1 don vi
 cout << "Now the variable num is " << num << endl;//xuat ra gia tri cua bien num
 return 0;
}